public class Potato {
	
	private int potato;
	
	//default constructor	
	
	public Potato()
	{
		this.potato = 180;
	}
	
	//parameterized constructor
	
	public Potato(int xPotato)
	{
		this.setPotato(xPotato);
	}
	
	public int Potato()
	{
		return potato;
	}
	
	public void setPotato(int xPotato)
	{
		this.potato = xPotato;
	}
	
	public int getPotato()
	{
		return potato;
	}
	
	
	
	
	
	
	
}
